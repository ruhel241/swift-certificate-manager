    public static function getDirStructure()
    {
        $workingPath = wp_upload_dir()['basedir'];
        $workingDir  = apply_filters('swift-certificate-manager/working_dir', $workingPath .'/'. SWIFT_CERTIFICATE_MANAGER_UPLOAD_DIR);
      
        return [
            'workingDir'      => $workingDir,
            'tempDir'         => apply_filters('swift-certificate-manager/pdf_temp_dir', $workingDir . '/temp/'),
            'pdfCacheDir'     => apply_filters('swift-certificate-manager/pdf_cache_dir', $workingDir . '/pdfCache/'),
            'fontDir'         => apply_filters('swift-certificate-manager/pdf_font_dir', $workingDir . '/fonts/'),
            'certificatesDir' => apply_filters('swift-certificate-manager/pdf_certificate_templates_dir', $workingDir . '/certificate_templates/')
        ];
    }

    public static function getDirUrlStructure()
    {
        $workingPathUrl = wp_upload_dir()['baseurl'];
        $workingUrl     = apply_filters('swift-certificate-manager/working_url', $workingPathUrl .'/'. SWIFT_CERTIFICATE_MANAGER_UPLOAD_DIR);
      
        return [
            'workingUrl'      => $workingUrl,
            'tempUrl'         => apply_filters('swift-certificate-manager/pdf_temp_url', $workingUrl . '/temp/'),
            'pdfCacheUrl'     => apply_filters('swift-certificate-manager/pdf_cache_url', $workingUrl . '/pdfCache/'),
            'fontUrl'         => apply_filters('swift-certificate-manager/pdf_font_url', $workingUrl . '/fonts/'),
            'certificatesUrl' => apply_filters('swift-certificate-manager/pdf_certificate_templates_url', $workingUrl . '/certificate_templates/')
        ];
    }

    public static function removedFile($filename) {

        if(empty($filename)) {
            return;
        }

        $getDirStructure = static::getDirStructure();
    
        $filepath =  $getDirStructure['tempDir'].$filename;
        
        if (!empty($filename) && file_exists($filepath)) {
            wp_delete_file($filepath);
        }     
    }

    public static function getSystemStatuses()
    {
        $mbString = extension_loaded('mbstring');
        $mbRegex = extension_loaded('mbstring') && function_exists('mb_regex_encoding');
        $gd = extension_loaded('gd');
        $dom = extension_loaded('dom') || class_exists('DOMDocument');
        $libXml = extension_loaded('libxml');
        $extensions = [
            'mbstring' => [
                'status' => $mbString,
                'label' => ($mbString) ? __('MBString is enabled', 'swift-certificate-manager') : __('The PHP Extension MB String could not be detected. Contact your web hosting provider to fix.', 'swift-certificate-manager')
            ],
            'mb_regex_encoding' => [
                'status' => $mbRegex,
                'label' => ($mbRegex) ? __('MBString Regex is enabled', 'swift-certificate-manager') : __('The PHP Extension MB String does not have MB Regex enabled. Contact your web hosting provider to fix.', 'swift-certificate-manager')
            ],
            'gd' => [
                'status' => $gd,
                'label' => ($gd) ? __('GD Library is enabled', 'swift-certificate-manager') : __('The PHP Extension GD Image Library could not be detected. Contact your web hosting provider to fix.', 'swift-certificate-manager')
            ],
            'dom' => [
                'status' => $dom,
                'label' => ($dom) ? __('PHP Dom is enabled', 'swift-certificate-manager') : __('The PHP DOM Extension was not found. Contact your web hosting provider to fix.', 'swift-certificate-manager')
            ],
            'libXml' => [
                'status' => $libXml,
                'label' => ($libXml) ? __('LibXml is OK', 'swift-certificate-manager') : __('The PHP Extension libxml could not be detected. Contact your web hosting provider to fix', 'swift-certificate-manager')
            ]
        ];

        $overAllStatus = $mbString && $mbRegex && $gd && $dom && $libXml;

        return [
            'status' => $overAllStatus,
            'extensions' => $extensions
        ];
    } 

    public static function cleanupTempDir()
    { 
        // $max_file_age = time() - 1 * 60; // 1 minute
        
        $max_file_age = time() - 6 * 3600; /* Max age is 6 hours old */
        $dirs = AvailableOptions::getDirStructure();
        $cleanUpDirs = [
            $dirs['tempDir'],
            $dirs['pdfCacheDir']
        ];

        foreach ($cleanUpDirs as $tmp_directory) {
            if (is_dir($tmp_directory)) {
              
                try {
                    $directory_list = new \RecursiveIteratorIterator(
                        new \RecursiveDirectoryIterator($tmp_directory, \RecursiveDirectoryIterator::SKIP_DOTS),
                        \RecursiveIteratorIterator::CHILD_FIRST
                    );
                    
                    foreach ($directory_list as $file) {
                        if ($file->isReadable() && $file->getMTime() < $max_file_age) {
                            if (!$file->isDir()) {
                                wp_delete_file($file);
                            }
                        }
                    }
                } catch (\Exception $e) {
                   //
                }
            }
        }
    }

